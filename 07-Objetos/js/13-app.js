// OBJECT LITERAL
const producto = {
    nombre : "Monitor de 20 pulgadas", // Propiedad o llave del objeto
    precio : 500,
    disponible : true
}

// KEYS
console.log( Object.keys(producto) ); // obtener sus llaves del objeto en un arreglo

// VALUES
console.log( Object.values(producto) ); // Obtener el value de los objetos en un arreglo

// ENRIES
console.log( Object.entries(producto) ); // ambos, key y value
